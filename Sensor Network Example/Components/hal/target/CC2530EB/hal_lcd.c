/*******************************************************************************
 * @fn      initLcd
 *
 * @brief   Initilize HW LCD Driver.
 *
 * @param   None
 *
 * @return  None
 ******************************************************************************/
void initLcd(void)
{
  LCD_CS_DDR |= LCD_CS_BV; 
  LCD_A0_DDR |= LCD_A0_BV; 
  LCD_SCL_DDR |= LCD_SCL_BV; 
  LCD_SI_DDR |= LCD_SI_BV; 
    
  lcdCommand(0xE2);                 //Reset Function
  lcbWait(2000);
  lcdCommand(0x2F);                 //Set Power Control
  lcbWait(2000);

  lcdCommand(0xF8);                 //Set BOOSTER RATIO SET 1
  lcdCommand(0x01);                 //Set BOOSTER RATIO SET 2 (5X)

  lcdCommand(0xA3);                 //Set 1/7 Bias
  lcdCommand(0x26);                 //Set V5 Ratio
  lcdCommand(0x81);                 //Set Electronic Mode
  lcdCommand(0x20);                 //Set Electronic EQUister 

  lcdCommand(0xC0);                 //COM output -> 0xC0:normal;0xC8:reverse
  lcdCommand(0xA1);                 //SEG output -> 0xA0:normal;0xA1:reverse
  lcdCommand(0xA6);                 //LCD display -> 0xA6:normal;0xA7:reverse
  lcdCommand(0xAF);                 //LCD display -> 0xAF:on;0xAE:off

  lcdCommand(0x40);                 //Start Line
  lcbWait(2000);

  HalLcd_HW_Clear();
}

/*******************************************************************************
 * @fn      HalLcd_HW_Control
 *
 * @brief   Write 1 command to the LCD
 *
 * @param   uint8 cmd - command to be written to the LCD
 *
 * @return  None
 ******************************************************************************/
//-------------------------------------------------------------------
// @fn          lcdCommand
// @brief       Send command to display
// @param       uint8 command
// @return      none
//-------------------------------------------------------------------
static void lcdCommand(uint8 command)
{
    uint8 i;
    HAL_LCD_A0_CLR();
    HAL_LCD_CS_CLR();
    for(i=0;i<8;i++)
    {
        if(command&(0x80>>i))
        {
            HAL_LCD_SI_SET();
        }
        else
        {
            HAL_LCD_SI_CLR();
        }
        HAL_LCD_SCL_CLR();
        HAL_LCD_SCL_SET();
    }
    HAL_LCD_CS_SET();
    HAL_LCD_SI_SET();
}

/*******************************************************************************
 * @fn      HalLcd_HW_Write
 *
 * @brief   Write 1 byte to the LCD
 *
 * @param   uint8 data - data to be written to the LCD
 *
 * @return  None
 ******************************************************************************/
//-------------------------------------------------------------------
// @fn          lcdData
// @brief       Write character to display
// @param       uint8 ch - character to display
// @return      none
//-------------------------------------------------------------------
static void lcdData(uint8 ch)
{
    uint8 i;
    HAL_LCD_A0_SET();
    HAL_LCD_CS_CLR();
    for(i=0;i<8;i++)
    {
        if(ch&(0x80>>i))
        {
            HAL_LCD_SI_SET();
        }
        else
        {
            HAL_LCD_SI_CLR();
        }
        HAL_LCD_SCL_CLR();
        HAL_LCD_SCL_SET();
    }
    HAL_LCD_CS_SET();
    HAL_LCD_SI_SET();
}

