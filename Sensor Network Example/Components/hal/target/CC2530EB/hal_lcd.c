/*******************************************************************************
 * @fn      HalLcd_HW_WriteChar
 *
 * @brief   Write one char to the display
 *
 * @param   uint8 line - line number that the char will be displayed
 *          uint8 col - colum where the char will be displayed
 *
 * @return  None
 ******************************************************************************/
/************************************************************
 * @fn      HalLcd_HW_WriteChar
************************************************************/
static void lcdUpdateChar( uint8 line, uint8 col, char ch )
{
    uint8 i,pag,colh,coll;

    pag = ((line-1)*2)+0xB0;
    col*=8;
    col+=4;
    colh = col>>4;
    colh = colh | 0xf0;
    colh = colh & 0x1f;
    coll = col & 0x0f;

    if(ch<32 || ch>127)
            ch=32;
    uint16 j=((uint16)(ch-32))*16;

    lcdCommand(colh);
    lcdCommand(coll);
    lcdCommand(pag);
    for (i=0;i<16;i++)
    { 
        if (i == 8)
        { 
            lcdCommand(pag+1);
            lcdCommand(colh);
            lcdCommand(coll);
        }
		lcdData(Font16x8[j++]);
    }
}

